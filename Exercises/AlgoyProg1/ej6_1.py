def imprimir_dos_primeros_caracteres(cadena):
    print(cadena[:2])

def imprimir_tres_ultimos_caracteres(cadena):
    print(cadena[-3:])

def imprimir_cada_dos_caracteres(cadena):
    print(cadena[::2])

def invertir_cadena(cadena):
    print(cadena[::-1])

def reflejar_cadena(cadena):
    print(cadena[:] + cadena[::-1])